           <h1>Bosquejo historico de la Fisica<small></small></h1>
           </div>
            <p style="font-size: 20px;">La Física tiene sus orígenes en la Grecia antigua, en donde se trató de explicar el origen del universo y el movimiento de los planetas. Su desarrollo ha estado ligado al progreso mismo de la humanidad. Básicamente se divide en cinco etapas principales: Primeras civilizaciones, Edad Media, Renacimiento, Periodo Clásico y Época Moderna,  en las que destacan científicos e investigadores cuyo trabajo ha revolucionado la ciencia y la tecnología del mundo.</p>
             <p style="font-size: 20px;">A finales del siglo XVII la Física comienza a influir en el desarrollo tecnológico permitiendo a su vez un avance más rápido de la propia Física.
A partir del Siglo XVIII Boyle y Young desarrollaron la Termodinámica. 
En 1733 Bernoulli usó argumentos estadísticos, junto con la Mecánica clásica, para extraer resultados de la Termodinámica, iniciando la Mecánica estadística.
En 1798 Thompson demostró la conversión del trabajo mecánico en calor y en 1847 Joule formuló la ley de la conservación de la energía.
En el campo de la Óptica, el siglo comenzó con la teoría corpuscular de la luz de Newton expuesta en su famosa obra "Opticks".</p>
<h1>Fisica Moderna</h1>
<p style="font-size: 20px;">También llamada Física Cuántica tiene sus comienzos a principios del siglo XX cuando Max Planck empieza sus investigaciones sobre el "cuanto" de energía.
Ésta se considera a partir de la teoría de la relatividad y de la teoría cuántica en la descripción de sistemas microscópicos (atómos y moléculas).
Estudia las manifestaciones que se producen en los átomos, los comportamientos de las partículas que forman la materia y las fuerzas que la rigen. 
Se relaciona con fenómenos como la fusión, la fisión nuclear, los superconductores, los sistemas en los dispositivos electrónicos y el láser. 
También explica el comportamiento de la materia y la energía a velocidades cercanas a la luz.
En 1905 Albert Einstein publicó una serie de trabajos que revolucionaron la Física.
Algunos de sus representantes más destacados son: Niels Bohr, Karl Heinsenberg, Ernest Rutherford y Albert Michelson.</p>
