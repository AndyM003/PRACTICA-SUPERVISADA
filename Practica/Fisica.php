           <h1>La Ciencia<small></small></h1>
           </div>
            <p style="font-size: 20px;">Se denomina ciencia a todo el conocimiento o saber constituido por una serie de principios y leyes que derivan de la observación y el razonamiento de un cúmulo de información y datos, los cuales son estructurados sistemáticamente para su comprensión.</p>
             <p style="font-size: 20px;">En este sentido, la ciencia comprende varios campos de conocimiento y estudio que conllevan al desarrollo de teorías y métodos científicos particulares, tras los cuales se pueden obtener conclusiones objetivas y verificables.

La ciencia, además, está íntimamente relacionada con el área de las ciencias exactas (matemática, física, química, ciencias naturales) y la tecnología. De allí la importancia de los estudios científicos destinados a crear o perfeccionar la tecnología ya existente, a fin de alcanzar una mejor calidad de vida.

La palabra ciencia deriva del latín scientĭa, que significa ‘conocimiento’, ‘saber’.</p>
