             <p>El alcance de la física es extraordinariamente amplio y puede incluir estudios tan diversos como la mecánica cuántica, la física teórica o la óptica.La física moderna se orienta a una especialización creciente, donde los investigadores tienden a enfocar áreas particulares más que a ser universalistas, como lo fueron Albert Einstein o Lev Landau, que trabajaron en una multiplicidad de áreas</p>.</p>
