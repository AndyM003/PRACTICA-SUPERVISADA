<h1>Fisica Clasica</h1 >
<p style="font-size: 20px;">En el siglo XVI Galileo fue pionero en el uso de experiencias para validar las teorías de la Física. Se interesó en el movimiento de los astros y de los cuerpos. Usando instrumentos como el plano inclinado, descubrió la ley de la inercia de la dinámica, y con el uso de uno de los primeros telescopios observó que Júpiter tenía satélites girando a su alrededor y las manchas solares del Sol. Estas observaciones demostraban el modelo heliocéntrico de Nicolás Copérnico y el hecho de que los cuerpos celestes no son perfectos e inmutables. 
En la misma época, las observaciones de Tycho Brahe y los cálculos de Johannes Kepler permitieron establecer las leyes que gobiernan el movimiento de los planetas en el Sistema Solar. 
En 1687 Newton publicó los Principios Matemáticos de la Naturaleza.
En el siglo XVI nacieron algunos personajes como Copérnico, Stevin, Cardano, Gilbert y Brahe. </p>
